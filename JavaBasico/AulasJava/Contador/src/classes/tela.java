        int f = sliderFim.getValue();
        int n = sliderPasso.getValue();
        
        DefaultListModel lista = new DefaultListModel(); //criar o objeto para usar a lista
        
        for(int c = i; c <= f; c+=n){
            lista.addElement(c);
        }
        
        listaItens.setModel(lista);
